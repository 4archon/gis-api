create table users (
    id              integer         primary key generated by default as identity (start with 45),
    login           varchar(30),
    password        varchar(30),
    role            varchar(15),
    active          boolean,
    name            varchar(30),
    surname         varchar(30),
    patronymic      varchar(30),
    -- group           varchar(20),
    tg_id           bigint
);

