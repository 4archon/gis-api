create table points (
    id              integer         primary key generated by default as identity (start with 3200),
    active          boolean,
    long            float8,
    lat             float8,
    address         varchar(150),
    district        varchar(100),
    number_arc      integer,
    arc_type        varchar(30),
    carpet          varchar(30),
    change_date     timestamp,
    comment         varchar(500)
);

create table service (
    id                  integer         primary key generated by default as identity,
    point_id            integer         references points(id),
    user_id             integer[],
    appointment_date    timestamp,
    deadline            timestamp,
    execution_date      timestamp,
    comment             varchar(500),
    status              varchar(100),
    sent                boolean
);

create table service_works (
    id              integer         primary key generated by default as identity,
    service_id      integer         references service(id),
    type            varchar(20),
    work            varchar(100),
    arc             integer
);

-- service_works type required, done

create table media (
    id              integer         primary key generated by default as identity,
    work_id         integer         references service_works(id),
    media_type      varchar(20),
    media_name      varchar(100)
);

create table tasks (
    id              integer         primary key generated by default as identity,
    point_id        integer         references points(id),
    type            varchar(100),
    comment         varchar(500),
    service_id      integer         references service(id)
);